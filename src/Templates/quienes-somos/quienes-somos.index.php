<div class="my-5">
    <h2>¿Quiénes somos?</h2>

    <p>
        En el proceso de tener un animal de compañía pueden existir diversas
        situaciones que, desgraciadamente para todos, acaben en la necesidad de
        la familia de tener que prescindir del animal, ya sea por la llegada de un
        hijo que impida la convivencia, el traslado de una familia a un piso donde
        no se permiten animales y otras diversas causas. El hecho de que la
        familia no pueda hacerse cargo no debería ser el detonante del abandono
        del animal, ya que pueden existir otras familias o personas que en ese
        momento estén buscando un nuevo animal que adoptar, y ambas partes
        puedan salir beneficiadas de un intercambio.
        Nuestro proyecto consiste en crear una plataforma donde estas familias
        puedan poner a su mascota en un lugar visible y una nueva familia pueda
        hacerse cargo de ellas. También en los casos de animales rescatados de la
        calle, cuando la persona que lo ha rescatado no pueda hacerse cargo del
        animal en el futuro y lo cuide hasta que aparezca otra familia.
    </p>
</div>
